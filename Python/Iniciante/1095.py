# Problema 1095 - URI - Iniciante - Nível 1

# Declaração das variáveis iniciais 'j' e 'i'
j=60
i=1

# Criação do loop while que realizará o laço ate 'j' ser menor que zero
while j >= 0:

    # Printando a saída 'i' e 'j'
    print(f'I={i} J={j}')

    # Diminuindo em 5 o valor da variável 'j' e incrementando em 3 a variável 'i'
    j=j-5
    i=i+3
